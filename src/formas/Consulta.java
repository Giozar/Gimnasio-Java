    public void llenarListaAsistentes() {
        
        for (int i = 0; i < lista.size(); i++) {
            cbListaAsistentes.addItem(lista.get(i).getNombre());
        }
    }

    public void llenarTablaInicial() {

        

        tbAsistentes.setModel(modelo);
        tbAsistentes.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

        LlenarTabla llenar = new LlenarTabla();
        Vector<String> listaTitulos = new Vector();
        listaTitulos = llenar.tituloColumnas();

        for (int i = 0; i < listaTitulos.size(); i++) {
            String titulo = listaTitulos.get(i);
            modelo.addColumn(titulo);
        }
        Vector filas = llenar.elementosCelda();
        modelo.setDataVector(filas, listaTitulos);
        tbAsistentes.setModel(modelo);
    }

    public void seleccionarAsistente() {
        // Agregar ListSelectionListener a la tabla
        tbAsistentes.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                // Obtener el índice de la fila seleccionada
                int rowIndex = tbAsistentes.getSelectedRow();

                // Verificar si hay una fila seleccionada
                if (rowIndex >= 0) {
                    // Obtener el modelo de la tabla
                    DefaultTableModel model = (DefaultTableModel) tbAsistentes.getModel();

                    // Obtener los datos de la fila seleccionada
                    Asistente asistenteSeleccionado = new Asistente();
                    asistenteSeleccionado = (Asistente) model.getValueAt(rowIndex, 0);
                    asistente = asistenteSeleccionado;
                }
            }
        });

    }
