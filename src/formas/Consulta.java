    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String nombre = lblBuscar.getText();
        Escritura buscar = new Escritura();
        
        int pos = buscar.buscarAsistente(lista, nombre);
        if(pos>=0){
            JOptionPane.showMessageDialog(null, "Asistente encontrado", "Busqueda exitosa", JOptionPane.INFORMATION_MESSAGE);
            asistente = lista.get(pos);
            Administrar administrar = new Administrar(asistente);
            administrar.setVisible(true);
            this.dispose();
        } else
            JOptionPane.showMessageDialog(null, "Asistente No se econtro", "Busqueda fallida", JOptionPane.INFORMATION_MESSAGE);

        
    }//GEN-LAST:event_btnBuscarActionPerformed

